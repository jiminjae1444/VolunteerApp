package com.example.myapplication_login_test;

public class Info {
    private Long userId;
    private String grade;
    private String volunteer_hours;

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getVolunteer_hours() {
        return volunteer_hours;
    }

    public void setVolunteer_hours(String volunteer_hours) {
        this.volunteer_hours = volunteer_hours;
    }
}
